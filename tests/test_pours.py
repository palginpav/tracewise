@skip_no_kicad
@skip_no_synth_fixture
def test_synthesize_power_pours_reduces_unconnected():
    """Core F3 gate: synthesize_power_pours reduces unconnected pads for +5V.

    Before synthesis the +5V pads are all unconnected (no zone exists).  After
    synthesis the fill connects at least some of them, so the unconnected count
    for +5V must drop.
    """
    import shutil
    import tempfile

    from tracewise.route.bridge import _run_pcbnew_script
    from tracewise.route.engine.pours import synthesize_power_pours

    count_script = """
import pcbnew, json
b = pcbnew.LoadBoard({board!r})
b.BuildConnectivity()
conn = b.GetConnectivity()
conn.RecalculateRatsnest()

pour_nets = set()
for z in b.Zones():
    nn = z.GetNetname()
    if nn:
        for layer_id in z.GetLayerSet().CuStack():
            if layer_id in (pcbnew.F_Cu, pcbnew.B_Cu) and z.HasFilledPolysForLayer(layer_id):
                pour_nets.add(nn)
                break

isolated_5v = 0
for fp in b.GetFootprints():
    for pad in fp.Pads():
        if pad.GetNetname() != "+5V":
            continue
        if "+5V" not in pour_nets:
            isolated_5v += 1
            continue
        connected = any(
            conn.IsConnectedOnLayer(pad, lid)
            for lid in pad.GetLayerSet().CuStack()
            if lid in (pcbnew.F_Cu, pcbnew.B_Cu)
        )
        if not connected:
            isolated_5v += 1
print("TWJSON" + json.dumps({{"isolated_5v": isolated_5v, "pour_nets": list(pour_nets)}}))
raise SystemExit(0)
"""

    with tempfile.TemporaryDirectory() as tmp:
        board = Path(tmp) / "synth_reduce.kicad_pcb"
        shutil.copy(SYNTH_BOARD, board)

        before_out = _run_pcbnew_script(count_script.format(board=str(board)))
        before = {}
        for line in before_out.splitlines():
            if line.startswith("TWJSON"):
                import json
                before = json.loads(line[len("TWJSON"):])

        before_isolated = before.get("isolated_5v", 10)

        # Synthesize pours (modifies board in-place, fills zones)
        synthesize_power_pours(board, min_pads=8)

        after_out = _run_pcbnew_script(count_script.format(board=str(board)))
        after = {}
        for line in after_out.splitlines():
            if line.startswith("TWJSON"):
                import json
                after = json.loads(line[len("TWJSON"):])

        after_isolated = after.get("isolated_5v", 10)

        assert after_isolated < before_isolated, (
            f"Expected fewer unconnected +5V pads after synthesis; "
            f"before={before_isolated} after={after_isolated}"
        )
