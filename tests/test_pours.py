"""F0 pour-extraction tests.

Guards and fixture
------------------
All tests that touch pcbnew are skipped when pcbnew is unavailable (same
pattern as existing pcbnew-dependent tests in the repo).  The fixture boards
are pre-generated by ``tests/fixtures/build_pour_fixture.py``; they must exist
before running the tests.

Fixture topology (F.Cu only, 30×30 mm, one GND zone):
  GND zone polygon: (1,1)→(20,1)→(20,20)→(1,20)
  R1 pad at  (5,  5) — inside zone → connected via fill
  R2 pad at (10, 10) — inside zone → connected via fill
  R3 pad at (25, 25) — outside zone → ISOLATED (the stitch worklist entry)

Two boards are tested:
  pour_source.kicad_pcb  — original build
  pour_resaved.kicad_pcb — pcbnew round-trip of source

The core invariant: identical results on both boards defeats the s-expr
net_name fragility described in F0-pour-extraction.md.
"""

from __future__ import annotations

import math
from pathlib import Path

import pytest

# ---------------------------------------------------------------------------
# pcbnew availability guard (mirrors test_engine.py pattern)
# ---------------------------------------------------------------------------

FIXTURE_DIR = Path(__file__).parent / "fixtures"
SOURCE_BOARD = FIXTURE_DIR / "pour_source.kicad_pcb"
RESAVED_BOARD = FIXTURE_DIR / "pour_resaved.kicad_pcb"


def _pcbnew_available() -> bool:
    try:
        import subprocess
        r = subprocess.run(
            ["python3", "-c", "import pcbnew"],
            capture_output=True, text=True, timeout=10,
        )
        return r.returncode == 0
    except Exception:
        return False


def _fixtures_exist() -> bool:
    return SOURCE_BOARD.exists() and RESAVED_BOARD.exists()


skip_no_kicad = pytest.mark.skipif(
    not _pcbnew_available(),
    reason="pcbnew not available (install KiCad)",
)
skip_no_fixtures = pytest.mark.skipif(
    not _fixtures_exist(),
    reason=(
        "Fixture boards missing — run "
        "python3 tests/fixtures/build_pour_fixture.py"
    ),
)

needs_kicad = pytest.mark.usefixtures()  # combined below per-test


