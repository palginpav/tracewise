@skip_no_kicad
@skip_no_fixtures
def test_extract_pours_polygon_geometry():
    """The GND pour must have at least one polygon with area ≈ zone area.

    The GND zone covers (1,1)→(20,20) = 19×19 = 361 mm².  The actual fill
    will be slightly smaller (clearance and thermal relief remove area), so we
    check that at least one polygon covers ≥ 300 mm².
    """
    from tracewise.route.engine.pours import extract_pours

    polys = extract_pours(SOURCE_BOARD)["GND"]
    # All polys should be on layer 0 (F.Cu) for this fixture
    assert all(p.layer == 0 for p in polys), "All fixture pours should be on F.Cu (layer 0)"
    total_area = sum(_poly_area(p.pts) for p in polys)
    assert total_area >= 200.0, f"Total GND pour area {total_area:.1f} mm² < expected minimum"


@skip_no_kicad
@skip_no_fixtures
def test_extract_pours_identical_across_formats():
    """Core F0 gate: extract_pours gives identical results on source vs resaved.

    This is the direct test of the anti-fragility claim in F0-pour-extraction.md:
    pcbnew's GetNetname() is stable across save-format variants, unlike s-expr
    net_name which can be absent on resaved boards.
    """
    from tracewise.route.engine.pours import extract_pours

    src = extract_pours(SOURCE_BOARD)
    rsv = extract_pours(RESAVED_BOARD)

    # Same net keys
    assert set(src.keys()) == set(rsv.keys()), (
        f"Net key mismatch: source={set(src.keys())} resaved={set(rsv.keys())}"
    )

    for net in src:
        src_polys = src[net]
        rsv_polys = rsv[net]
        # Same number of polygons
        assert len(src_polys) == len(rsv_polys), (
            f"net={net}: polygon count source={len(src_polys)} resaved={len(rsv_polys)}"
        )
        for i, (sp, rp) in enumerate(zip(src_polys, rsv_polys, strict=True)):
            # Same layer
            assert sp.layer == rp.layer, f"net={net} poly {i}: layer mismatch"
            # Same point count
            assert len(sp.pts) == len(rp.pts), (
                f"net={net} poly {i}: point count {len(sp.pts)} vs {len(rp.pts)}"
            )
            # Same area within 0.1 mm² tolerance
            area_src = _poly_area(sp.pts)
            area_rsv = _poly_area(rp.pts)
            assert math.isclose(area_src, area_rsv, abs_tol=0.1), (
                f"net={net} poly {i}: area source={area_src:.3f} resaved={area_rsv:.3f}"
            )


