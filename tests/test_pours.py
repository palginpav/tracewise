def _make_grid(w: float = 20.0, h: float = 20.0, pitch: float = 0.1):
    from tracewise.route.engine.grid import Grid

    return Grid(x0=0.0, y0=0.0, width_mm=w, height_mm=h, pitch=pitch)


def test_rasterize_pour_marks_interior():
    """Interior cells of a simple rectangle pour must be True, exterior False.

    Pour: (2,2)→(8,8) on layer 0.  Grid: 20×20 mm, pitch 0.1 mm.
    Known interior point: (5, 5) → cell (50, 50).
    Known exterior point: (1, 1) → cell (10, 10).
    """
    from tracewise.route.engine.pours import PourPoly, rasterize_pour

    grid = _make_grid()
    rect_pts = [(2.0, 2.0), (8.0, 2.0), (8.0, 8.0), (2.0, 8.0)]
    polys = [PourPoly(net="GND", layer=0, pts=rect_pts)]
    mask = rasterize_pour(grid, polys)

    assert mask.shape == (2, grid.ny, grid.nx)
    assert mask.dtype == bool

    # Interior
    iy_in, ix_in = grid.to_cell(5.0, 5.0)
    assert mask[0, iy_in, ix_in], "Centre of pour rectangle should be True"

    # Exterior
    iy_out, ix_out = grid.to_cell(1.0, 1.0)
    assert not mask[0, iy_out, ix_out], "Outside pour rectangle should be False"

    # Correct layer only
    assert not mask[1, iy_in, ix_in], "Layer 1 should be untouched"


def test_rasterize_pour_layer_separation():
    """A pour on layer 1 must not affect layer 0."""
    from tracewise.route.engine.pours import PourPoly, rasterize_pour

    grid = _make_grid()
    polys = [PourPoly(net="GND", layer=1, pts=[(3.0, 3.0), (7.0, 3.0), (7.0, 7.0), (3.0, 7.0)])]
    mask = rasterize_pour(grid, polys)

    iy, ix = grid.to_cell(5.0, 5.0)
    assert mask[1, iy, ix], "Layer 1 interior should be True"
    assert not mask[0, iy, ix], "Layer 0 should be untouched"


def test_rasterize_pour_union_across_polys():
    """Two disjoint polygons on the same layer produce a union mask."""
    from tracewise.route.engine.pours import PourPoly, rasterize_pour

    grid = _make_grid()
    poly_a = PourPoly(net="GND", layer=0, pts=[(1.0, 1.0), (4.0, 1.0), (4.0, 4.0), (1.0, 4.0)])
    poly_b = PourPoly(net="GND", layer=0, pts=[(6.0, 6.0), (9.0, 6.0), (9.0, 9.0), (6.0, 9.0)])
    mask = rasterize_pour(grid, [poly_a, poly_b])

    iy_a, ix_a = grid.to_cell(2.5, 2.5)
    iy_b, ix_b = grid.to_cell(7.5, 7.5)
    iy_gap, ix_gap = grid.to_cell(5.0, 5.0)

    assert mask[0, iy_a, ix_a], "Centre of poly_a should be True"
    assert mask[0, iy_b, ix_b], "Centre of poly_b should be True"
    assert not mask[0, iy_gap, ix_gap], "Gap between polygons should be False"


def test_rasterize_pour_off_by_one_at_bbox_edges():
    """Cells at the interior corners of the bbox must be True; just outside False.

    The bbox of a 4×4 mm square at (2,2)→(6,6) has corners at exactly those
    coords.  The cell AT (2,2) has its centre at (2,2) which lies ON the edge
    and the Jordan test treats it as interior (crossing parity).  The cell at
    (1.9, 1.9) is strictly outside and must be False.
    """
    from tracewise.route.engine.pours import PourPoly, rasterize_pour

    grid = _make_grid()
    # 4×4 mm square, strictly defined corners
    polys = [PourPoly(net="GND", layer=0, pts=[(2.0, 2.0), (6.0, 2.0), (6.0, 6.0), (2.0, 6.0)])]
    mask = rasterize_pour(grid, polys)

    # Well inside
    iy_in, ix_in = grid.to_cell(4.0, 4.0)
    assert mask[0, iy_in, ix_in], "Well-interior cell must be True"

    # Well outside
    iy_out, ix_out = grid.to_cell(1.0, 1.0)
    assert not mask[0, iy_out, ix_out], "Well-exterior cell must be False"


