@skip_no_kicad
@skip_no_fixtures
def test_rasterize_pour_with_real_extract():
    """End-to-end: rasterize the GND pour from the fixture board.

    Verifies that the polygon returned by extract_pours rasterizes to at
    least one True cell and that the True region covers a reasonable fraction
    of the zone area (between 1% and 90% of the grid).
    """
    from tracewise.route.engine.grid import Grid
    from tracewise.route.engine.pours import extract_pours, rasterize_pour

    polys_by_net = extract_pours(SOURCE_BOARD)
    gnd_polys = polys_by_net.get("GND", [])
    assert gnd_polys, "No GND pour found in fixture board"

    # Build a grid that covers the fixture board (30×30 mm)
    grid = Grid(x0=0.0, y0=0.0, width_mm=30.0, height_mm=30.0, pitch=0.1)
    mask = rasterize_pour(grid, gnd_polys)

    true_count = mask.sum()
    total_cells = grid.layers * grid.ny * grid.nx
    fraction = true_count / total_cells

    assert true_count > 0, "rasterize_pour produced all-False mask for real fixture"
    assert fraction < 0.9, f"Suspiciously large pour fraction: {fraction:.3f}"
    assert fraction > 0.001, f"Suspiciously small pour fraction: {fraction:.4f}"

    # The pour should be on layer 0 (F.Cu) only for this fixture
    assert mask[0].any(), "F.Cu layer (0) should have True cells"
    assert not mask[1].any(), "B.Cu layer (1) should be empty for this fixture"


